function getNestedValue(obj, path) {
  return path.split(".").reduce((acc, key) => (acc && acc[key] !== undefined ? acc[key] : undefined), obj);
}

function applyTranslations(lang) {
  const current = translations[lang] || translations.sv;
  const isRtl = lang === "ar" || lang === "fa";

  document.documentElement.lang = lang;
  document.documentElement.dir = isRtl ? "rtl" : "ltr";
  document.body.classList.toggle("is-rtl", isRtl);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = getNestedValue(current, element.dataset.i18n);
    if (value !== undefined) {
      element.textContent = value;
    }
  });

  const pageKey = document.body.dataset.page;
  if (pageKey) {
    const translatedTitle = getNestedValue(current, `meta.${pageKey}Title`);
    const translatedDescription = getNestedValue(current, `meta.${pageKey}Description`);

    if (translatedTitle) {
      document.title = translatedTitle;
    }

    if (translatedDescription) {
      const metaDescription = document.querySelector('meta[name="description"]');
      if (metaDescription) {
        metaDescription.setAttribute("content", translatedDescription);
      }
    }
  }

  const selector = document.getElementById("language-select");
  if (selector) {
    selector.value = lang;
  }
}

function detectInitialLanguage() {
  const saved = localStorage.getItem("siteLanguage");
  if (saved && supportedLanguages.includes(saved)) {
    return saved;
  }

  const browserLanguage = (navigator.language || "sv").slice(0, 2).toLowerCase();
  return supportedLanguages.includes(browserLanguage) ? browserLanguage : "sv";
}

document.addEventListener("DOMContentLoaded", () => {
  const selector = document.getElementById("language-select");
  const initialLanguage = detectInitialLanguage();

  if (selector) {
    selector.addEventListener("change", (event) => {
      const selectedLanguage = event.target.value;
      localStorage.setItem("siteLanguage", selectedLanguage);
      applyTranslations(selectedLanguage);
    });
  }

  applyTranslations(initialLanguage);
});
